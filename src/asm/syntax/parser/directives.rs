pub(super) fn parse_println<'ctx_stack>(
    keyword: Token<'ctx_stack>,
    parse_ctx: &mut ParseCtx<'ctx_stack, '_, '_, '_, '_>,
) -> Option<Token<'ctx_stack>> {
    // TODO: support multiple elements
    let (expr, lookahead) = misc::parse_str_or_const_expr(parse_ctx.next_token(), parse_ctx);
    match expr {
        None => {}
        Some(misc::StrOrNum::Num(value)) => println!("{value}"),
        Some(misc::StrOrNum::String(string)) => println!("{string}"),
    };
    lookahead
}

